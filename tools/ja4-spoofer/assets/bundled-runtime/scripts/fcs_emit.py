EMITTERS = {
    "nss": emit_nss,
    "openssl": emit_openssl,
    "boringssl": emit_boringssl,
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Emit library-specific config from an FCS profile"
    )
    parser.add_argument("--input", required=True, help="Path to FCS profile (.yaml/.json)")
    parser.add_argument("--target", required=True, choices=sorted(EMITTERS.keys()))
    parser.add_argument("--output", required=True, help="Output file path")
    return parser.parse_args()


def main() -> int:
    args = parse_args()

    input_path = pathlib.Path(args.input).expanduser().resolve()
    output_path = pathlib.Path(args.output).expanduser().resolve()

    profile = _load_profile(input_path)
    rendered = EMITTERS[args.target](profile)

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(rendered, encoding="utf-8")
    print(f"wrote {args.target} config: {output_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
